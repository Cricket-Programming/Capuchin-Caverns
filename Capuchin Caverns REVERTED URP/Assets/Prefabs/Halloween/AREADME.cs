LIGHTING
For the environment to be dark, we need to change the AMBIENT LIGHT to DARK BLUE.
1. Ambient light
Ambient light is a default light, which illuminates all game objects in the scene evenly from all
directions.
I.e. Ambient light is used to adjust the overall look and feel of the environment with some
specific color and brightness, so that we can create different moods
Ex.
Sunny day mood (bright orange),
Dark night mood (black),
Sad mood (light gray),
Happy mood (light pink),
Horror mood (dark blue), Etc.
Adjusting Ambient light:
Window Menu - Lighting - Scene Tab - Environment Lighting - Source - Color - Adjust the
Ambient color

Also, make sure to mark objects you want affected by ambient light to be marked non static. 
Things you still want to be illuminated with baked lighting (like horror or the shop) should have static. 
Ambient lighting only works with nonstatic gameobjects.

